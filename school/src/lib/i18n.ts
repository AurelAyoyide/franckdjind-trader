export const locales = ["fr", "en"] as const;
export type Locale = (typeof locales)[number];

export const LOCALE_COOKIE = "school_locale";

export function isLocale(value: string | null | undefined): value is Locale {
  return value === "fr" || value === "en";
}

export function localeFromPath(pathname: string): { locale: Locale; pathname: string } {
  if (pathname === "/en") {
    return { locale: "en", pathname: "/" };
  }

  if (pathname.startsWith("/en/")) {
    return { locale: "en", pathname: pathname.slice(3) };
  }

  return { locale: "fr", pathname };
}

export function localePath(locale: Locale, pathname: string) {
  if (locale !== "en" || /^https?:\/\//.test(pathname) || pathname.startsWith("#")) {
    return pathname;
  }

  if (pathname === "/") {
    return "/en";
  }

  return pathname.startsWith("/en/") || pathname === "/en" ? pathname : `/en${pathname}`;
}

export function translate(locale: Locale, source: string) {
  return locale === "en" ? englishPhrases[source] ?? source : source;
}

